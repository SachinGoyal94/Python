#logical  and or not
print(1 and 0)
print(1 or 0)
print(not 1)

#bitwise | & ^ >> << ~ ^
print(3 | 2)
print(3 & 2)
print(3 ^ 2)
print(~3)
print(4<<2)
print(4>>2)

#membership operators  in   not in
print(1 in [1,2,3,4])
print(1  not in [1,2,3,4])
